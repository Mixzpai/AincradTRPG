namespace SAOTRPG.Inventory.Core;

// All valid equipment slots — extend this enum when adding new gear types
public enum EquipmentSlot
{
    Weapon,     // Swords, axes, spears, etc.
    Head,       // Helmets, circlets
    Chest,      // Chest armor, robes
    Legs,       // Leggings, greaves
    Feet,       // Boots, sandals
    RightRing,  // Ring slot 1
    LeftRing,   // Ring slot 2
    Bracelet,   // Wrist accessory
    Necklace    // Neck accessory
}
